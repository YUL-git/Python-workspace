# \n : 줄바꿈
print("백문이 불여일견 \n백견이 불여일타") #두줄에 걸쳐서 실행하고 싶어

#저는 "나도 코딩"입니다
print("저는 '나도코딩'입니다.") #작은 따옴표와 큰따옴표 사용
print('저는 "나도코딩"입니다')
print("저는 \"나도코딩\"입니다.") #문장내에서 큰 따옴표를 사용할 수 있게 해줘
print("저는\'나도코딩\'입니다")

# \\ : 문장 내에서 \로 바뀌게 됨
#print("\Users\hamdoyun\Desktop\Python workspace") # \ 하나때문에 에러가 떠
print("\\Users\\hamdoyun\\Desktop\\Python workspace") # \\써줌으로써 탈출문자가 됨

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine") # \r을 통해서 커서를 맨 앞으로 이동 Red 를 Pine으로 바꿈

# \b : 백스페이스(한 글자 삭제)
print("Redd\bApple")

# \t : 탭 역할 (4칸 8칸씩 움직임)
print("Red\tApple") #키보드에서 탭을 친것과 같은 효과






